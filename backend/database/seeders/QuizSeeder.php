        Spot::query()->orderBy('id')->each(function (Spot $spot): void {
            // スポット名をキーにして、その土地専用の4択クイズを取得します。
            $quizzes = $this->quizzesFor($spot->name);

            if ($quizzes === []) {
                return;
            }

            $questions = collect($quizzes)->pluck('question');

            // Seederを再実行した時に、古いテンプレート問題が残らないよう削除します。
